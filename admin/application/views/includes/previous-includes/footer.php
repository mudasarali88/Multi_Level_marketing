function cleanString(str)
{
	var newStr = str.replace(/[0-9`~!@#$%^&*()_|+\-=?;:'",.<>\{\}\[\]\\\/]/gi,'-');
	newStr = newStr.toLowerCase();
	newStr = newStr.split(' ').join('-');
	return newStr;
}
